const message : string = "Hello, TypeScript!";
console.log(message);

//here const is a constant variable that cannot be reassigned. The type of the variable is explicitly declared as string, which means it can only hold string values. The value assigned to the variable is "Hello, TypeScript!", and it is logged to the console using console.log().

//message is the variable name.
//string is the type of the variable, and it it knows as type annotation. It tells TypeScript that the variable message can only hold string values.


